                        <h2 id="heading-3">Профиль соискателя</h2>
                        <p>Заполните профиль один раз — и вся информация из профиля автоматически подтянется во все ваши резюме. При изменении данных в профиле резюме обновятся сами: править каждое по отдельности больше не нужно. 
                        <br><br>
                        Во вкладке <a href="#">«Резюме и профиль»</a> нажмите на блок с вашим именем — вы перейдёте на страницу профиля.</p>
                        <p>Профиль соискателя на hh.ru содержит основную информацию о вас и настройки поиска по вакансиям. </p>
                        <ol>
                            <li>Если у вас уже есть резюме на hh.ru, основная информация о вас будет перенесена из него в профиль. Вы можете дополнить или изменить ее, нажав на кнопку «Редактировать». </li>
                            <li>Фото в профиле также подтянется из резюме. Чтобы изменить его, перейдите к <a href="#">редактированию резюме</a>.</li>
                            <li>Дополните или отредактируйте контактную информацию. Вы можете добавить мессенджеры и соцсети для общения с работодателем. </li>
                            <li>Настройте поиск по вакансиям:
                                <ul>
                                    <li>укажите статус поиска;</li>
                                    <li>укажите место, где вы живёте;</li>
                                    <li>укажите регион поиска работы или выберите настройку «Все регионы».</li>
                                </ul>
                            </li>
                            <li>Образование. Укажите уровень образования и учебное заведение. Можно добавить несколько.
                                <ul>
                                    <li>По умолчанию в профиле стоит значение «Среднее», и оно автоматически подставляется во все ваши резюме.</li>
                                </ul>                               
                            </li>                            
                        </ol>
                        <h3>Как изменить:</h3>
                        <ul>
                            <li>Нажмите на стрелку справа от поля.</li>
                            <li>Выберите актуальный уровень и добавьте учебное заведение.</li>
                        </ul>
                        <ol>
                            <li>Укажите опыт вождения и наличие личного автомобиля.</li>
                            <li>Укажите, какие языки вы знаете и уровень владения ими.</li>
                            <li>Перейдите к тестированию для подтверждения навыков.</li>
                        </ol>
                        <p>Ваши навыки — ваша ценность. Подтвердите их с помощью объективных тестов и превратите резюме в мощный инструмент для карьерного роста. <a href="#">Читайте подробнее о подтверждении навыков.</a></p>

                        <h2 id="heading-4">Как искать вакансии</h2>
                        <p>Нажмите «Поиск» на верхней панели, чтобы искать по профессии, должности или компании. Нажмите кнопку фильтра справа от «Найти» для расширенного поиска. Подробнее о поиске вакансий.
                        <br><br>
                        Также вы можете воспользоваться автопоиском вакансий и получать актуальную информацию в мессенджер или на почту.
                        <br><br>
                        На главной странице личного кабинета соискателя в блоке «Вакансии для вас» размещены подходящие вакансии, подобранные для вас умным поиском.
                        </p>

                        <h2 id="heading-5">Как откликнуться на вакансию</h2>
                        <p>Для отклика на вакансию нажмите «Откликнуться», далее выберите одно из своих резюме (если их несколько) и заполните графу «Сопроводительное письмо». Нажмите на кнопку «Откликнуться», и ваше резюме будет отправлено работодателю. 
                        <br><br>
                        Если у вас нет ни одного резюме, то при нажатии кнопки «Откликнуться» вы будете перенаправлены к его созданию, а название должности автоматически подтянется из вакансии.
                        <br><br>
                        Подробнее о том, как откликнуться на вакансию.</p>

                        <h2 id="heading-6">Сопроводительное письмо</h2>
                        <p>Приложите к резюме сопроводительное письмо, чтобы сделать отклик более привлекательным и получить дополнительное преимущество среди других кандидатов.</p>

                        <h2 id="heading-7">Как откликнуться на вакансию без резюме</h2>
                        <p>Вы можете начать поиск вакансий без регистрации в поисковой строке на главной странице hh.ru.
                        <br><br>
                        При отклике на интересующую вакансию вы будете направлены на страницу ввода контактных данных. Подробнее о том, как откликнуться на вакансию без резюме.</p>

                        <h2 id="heading-8">Общение с работодателем</h2>
                        <p>Зачастую соискателям и работодателям может потребоваться дополнительная информация о вакансии или резюме. Существует несколько способов, при помощи которых вы можете общаться с работодателем.
                        <br><br>
                        Подробнее об общении с работодателем.</p>

                        <h2 id="heading-9">Отклики и приглашения</h2>
                        <p>Новые отклики вы сможете увидеть на главной странице личного кабинета в блоке слева. Чтобы узнать подробности, зайдите в раздел «Отклики».
                        <br><br>
                        Если работодатель просмотрел страницу с резюме, но не отправил приглашение или отказ, то у такого отклика появится статус «Резюме просмотрено». Подробнее о просмотре откликов работодателем.</p>
                        <p>Как только работодатель направит вам приглашение или отказ вы увидите уведомления на главной странице в блоке «Отклики и приглашения» и в разделе верхнего меню «Отклики».
                        <br><br>
                        Перейдите на страницу откликов и приглашений, чтобы увидеть подробности.
                        <br><br>
                        Чтобы точно не пропустить приглашение от работодателя, настройте уведомления о статусе ваших откликов в мессенджер.</p>
