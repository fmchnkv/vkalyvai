                        <h2 id="heading-1">Регистрация</h2>
                        <p>Для начала работы с вкалывай.рф мы рекомендуем пройти регистрацию. В этом случае вы сразу сможете отправлять резюме на интересные вам вакансии.</p>
                        <ul>
                            <li>Для регистрации нажмите «Создать резюме» на верхней панели сайта hh.ru.</li>
                            <li>Далее укажите свой адрес электронной почты или номер телефона и нажмите «Зарегистрироваться».</li>
                        </ul>

                        <h2 id="heading-2">Создание резюме</h2>
                        <p>Резюме нужно, чтобы показать работодателям ваш опыт, навыки, ценные личные качества. Качественное резюме привлечёт к вам внимание работодателей и поможет быстрее получить приглашение на работу. <a href="#">Подробнее о том, как создать резюме.</a>
                        <br><br>
                        <a href="#">Во вкладке «Резюме и профиль»</a> вы можете в любой момент <a href="#">отредактировать своё резюме</a>, изменить его <a href="#">настройки видимости</a> или <a href="#">удалить</a>.
                        <br><br>
                        Если вы ищете работу впервые и пока не имеете опыта, читайте о том, <a href="#">как искать работу без опыта</a>.</p>
